package JAVA_Basics;

public class Arithmetic {

	public void multiply(int a, int b)
	{
		System.out.println("Multiplication of 2 Int" +(a*b));
	}
	public void multiply(float a, int b)
	{
		System.out.println("Multiplication of 1 float and 1 Int" +(a*b));
	}
	public void multiply(double a, double b)
	{
		System.out.println("Multiplication of 2 double" +(a*b));
	}
	public void multiply(int a, int b,int c)
	{
		System.out.println("Multiplication of 3 Int" +(a*b*c));
	}
}
