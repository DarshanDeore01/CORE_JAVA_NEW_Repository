package JAVA_Basics;

public class Calculator {


	    public static void main(String[] args) {
	        Arithmetic arithmetic = new Arithmetic();

	        // Multiplication of two integers
	        arithmetic.multiply(5, 10);

	        // Multiplication of one float and one integer
	        arithmetic.multiply(3.5f, 7);

	        // Multiplication of two doubles
	        arithmetic.multiply(2.5, 4.5);

	        // Multiplication of three integers
	        arithmetic.multiply(2, 3, 4);
	    }
	  

}
